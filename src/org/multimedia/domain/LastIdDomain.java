package org.multimedia.domain;

public class LastIdDomain {
	private int uorr;
	private int num;
	
	public LastIdDomain(int uorr, int num) {
		this.uorr = uorr;
		this.num = num;
	}
	
	public int getUorr() {
		return uorr;
	}
	public void setUorr(int uorr) {
		this.uorr = uorr;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
	
}
